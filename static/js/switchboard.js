function toggleButtonSwitchLED(e) {
  var switchButton = document.getElementById("LED");
  
  var toggleValue = "";
  if (switchButton.checked) {
    console.log("On!");
    toggleValue = "ON";
  } else {
    console.log("Off!");
    toggleValue = "OFF"
  }
  fetch( "/LED")
  .then( response => {
    console.log(response);
  } )
}
